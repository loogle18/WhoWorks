import UIKit

extension UIColor {
    class func customBlack() -> UIColor {
        return UIColor(red: 21/255, green: 21/255, blue: 21/255, alpha: 1.0)
    }
    
    class func customSecondaryBlack() -> UIColor {
        return UIColor(red: 41/255, green: 41/255, blue: 41/255, alpha: 1.0)
    }
    
    class func customWhite() -> UIColor {
        return UIColor(red: 244/255, green: 244/255, blue: 244/255, alpha: 1.0)
    }
    
    class func customRed() -> UIColor {
        return UIColor(red: 255/255, green: 48/255, blue: 48/255, alpha: 1.0)
    }
    
    class func customYellow() -> UIColor {
        return UIColor(red: 255/255, green: 219/255, blue: 75/255, alpha: 1.0)
    }
    
    class func customGreen() -> UIColor {
        return UIColor(red: 4/255, green: 219/255, blue: 126/255, alpha: 1.0)
    }
    
    class func customGray() -> UIColor {
        return UIColor(red: 190/255, green: 190/255, blue: 190/255, alpha: 1.0)
    }
    
    class func customLightGray() -> UIColor {
        return UIColor(red: 206/255, green: 206/255, blue: 206/255, alpha: 1.0)
    }
}
